        <h4>¿Qué son las Variables?</h4>
        <br><br>
        Una variable consiste en un elemento al cual le damos un nombre y le atribuimos un determinado tipo de información.<br><br>
        Las variables pueden ser consideradas "cajas" que nos permiten guardar informacion temporalmente para luego ser utilizada.
        <br><br>        <img src="http://img.c4learn.com/2012/02/Variable-in-Java.jpg" alt=""><br><br>
        <br>
        De este modo podríamos escribir que:
        <br>
        variable <b>a</b> = "perro"<br>
        variable <b>b</b> = "muerde"<br>
        <br>
        La variable que nosotros llamamos "a" posee un elemento de información de tipo texto que es "perro".
        Asimismo, la variable "b" contiene el valor "muerde".
        Podríamos definir una tercera variable que fuese la suma de estas dos:
        <br>
        variable <b>c</b> = <b>a</b> + <b>b</b>
        <br><br>
        Si introdujésemos una petición de impresión de esta variable en nuestro lenguaje ficticio:
        <br><br>
        imprimir(c)
        <br>
        El resultado podría ser:
        <br>
        >> perro muerde
        <br>
        Podríamos de la misma forma trabajar con variables que contuviesen números y construir nuestro programa:

        <b>a</b> = 3<br>
        <b>b </b>= 4<br>
        <b>c</b> = <b>a</b> + <b>b</b><br>
        imprimir(<b>c</b>)<br>
        <br>
        El resultado de nuestro programa sería:
        <br>
        >> <b>7</b>
