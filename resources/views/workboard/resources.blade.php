        <h4>Utilizando Condicionales</h4>

        En programación, una sentencia <b>condicional</b> es una instrucción o grupo de instrucciones que se pueden ejecutar o no dependiendo de la veracidad de su condición.

        Los tipos más conocidos de sentencias condicionales son el SI..ENTONCES (if..then), el SI..ENTONCES..SI NO (if..then..else)
