        <h4>R M B</h4>
        El
        <b>ritmo metabólico basal</b> o metabolismo basal es el valor mínimo de energía necesaria para que la célula subsista.
        <br><br>Esta energía mínima es utilizada por la célula en las reacciones químicas intracelulares necesarias para la realización de funciones metabólicas esenciales, como es el caso de la respiración.
        <br><br>
        En el organismo, el metabolismo basal depende de varios factores, como sexo, talla, peso, edad, etc. Como claro ejemplo del metabolismo basal está el caso del coma. La persona «en coma», está inactiva, pero tiene un gasto mínimo de calorías, razón por la que hay que seguir alimentando al organismo.
